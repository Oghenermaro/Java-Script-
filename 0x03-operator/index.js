//incremental and decremental operator
let num = 4;
num++;

console.log(num);

let num2 = 5;
num2--
console.log(num2);

//prefix
let num3 = 6;
console.log(num3++)

console.log(num3)

//prefix
let num4 = 8;
console.log(++num4)

//extra nuggets
let num5 = 10;
num5 = num5 + 5;
console.log(num5)

//or
num5 += 5;

//comparison operator
//Equal to
let num6 = 5;
let num7 = "5";

console.log(num6 == num7);

// strict equal to
console.log(num6 === num7);

//Not equal to
let num8 = 5;
let num9 = 5;

console.log(num8!==num9);

//Greater than and Less than
let num10 = 14;
let num11 = 12;

console.log(num10 > num11)
console.log(num10 < num11)

//Greater than or equal to
console.log(num10 > num11)
console.log(num10 < num11)

